#include <stdio.h>

int main(void)
{
    int foo, bar, faz;
    float poo, bro, lol;

    printf("foo: %d, bar: %d, faz: %d\n", foo, bar, faz);
    printf("poo: %f, bro: %f, lol: %f\n", poo, bro, lol);

    return 0;
}

